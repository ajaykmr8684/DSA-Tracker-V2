// Difficulty pill with dot indicator
export function DiffBadge({ diff }: { diff: string }) {
  const config = {
    Easy:   { dot: '#22c55e', text: '#22c55e', bg: 'rgba(34,197,94,0.1)',   border: 'rgba(34,197,94,0.22)',  shadow: '0 0 8px rgba(34,197,94,0.2)'  },
    Medium: { dot: '#f59e0b', text: '#f59e0b', bg: 'rgba(245,158,11,0.1)',  border: 'rgba(245,158,11,0.22)', shadow: '0 0 8px rgba(245,158,11,0.2)' },
    Hard:   { dot: '#ef4444', text: '#ef4444', bg: 'rgba(239,68,68,0.1)',   border: 'rgba(239,68,68,0.22)',  shadow: '0 0 8px rgba(239,68,68,0.2)'  },
  }
  const c = config[diff as keyof typeof config] ?? config.Medium
  return (
    <span className="inline-flex items-center gap-1.5 px-2.5 py-1 rounded-full text-[11px] font-bold whitespace-nowrap"
      style={{ background: c.bg, color: c.text, border: `1px solid ${c.border}`, boxShadow: c.shadow }}>
      <span className="w-1.5 h-1.5 rounded-full flex-shrink-0" style={{ background: c.dot, boxShadow: `0 0 5px ${c.dot}` }} />
      {diff}
    </span>
  )
}

// Company brand icon in a circle
export function CompanyBadge({ company }: { company: string }) {
  const wrapStyle = {
    Google:    { bg: 'rgba(66,133,244,0.1)',  border: 'rgba(66,133,244,0.2)'  },
    Amazon:    { bg: 'rgba(255,153,0,0.1)',   border: 'rgba(255,153,0,0.2)'   },
    Meta:      { bg: 'rgba(24,119,242,0.1)',  border: 'rgba(24,119,242,0.2)'  },
    Microsoft: { bg: 'rgba(127,186,0,0.1)',   border: 'rgba(127,186,0,0.15)'  },
  }
  const style = wrapStyle[company as keyof typeof wrapStyle]
  if (!style) return null
  return (
    <span title={company} className="inline-flex items-center justify-center flex-shrink-0 rounded-full transition-transform hover:scale-110"
      style={{ width: 20, height: 20, background: style.bg, border: `1px solid ${style.border}` }}>
      {company === 'Google'    && <GoogleLogo size={11} />}
      {company === 'Amazon'    && <AmazonLogo size={11} />}
      {company === 'Meta'      && <MetaLogo size={11} />}
      {company === 'Microsoft' && <MicrosoftLogo size={11} />}
    </span>
  )
}
